/*
 Clone  = копирование репозитория локально
 Commit = Сохранить («контрольная точка») изменения в нашей текущей ветке.
 Stage = подготовка изменений для фиксации
 Stash = сохранить изменения на потом (локально)
 Push = отправить локальные коммиты в удаленный репозиторий
 Pull = Получить удаленные коммиты в локальное репозиторий
 Merge(Слияние) = объединение двух разных ветвей
 Rebase(Перебазировать) = перемещение одной ветки сверху или другой ветки
 Cherry picking = Дублирование (копирование) одной кукурузы с одной ветки на другую
 Pull Requet(PR) = Запрос на маргинализацию ветки
 
 Основные правила оформления:
 
 1. Заголовок (Subject): не более 50–72 символов. Пишется в повелительном наклонении (например, «Add feature», а не «Added feature»).
 2. Тип коммита: заголовок начинается с префикса, указывающего на характер изменений:
        feat: новая функциональность.
        fix: исправление ошибки.
        docs: изменения в документации.
        style: правки форматирования (пробелы, запятые), не влияющие на логику.
        refactor: правки кода без смены логики и исправления ошибок.
        test: добавление или обновление тестов.
        chore: технические задачи (обновление зависимостей, конфигов).
 3. Пустая строка: между заголовком и подробным описанием (телом) всегда должна быть пустая строка.
 4. Тело (Body): объясняет, что и зачем было сделано (не как). Строки не должны превышать 72 символа для удобства чтения в терминале.
 
 Примеры комментариев:
 
    Простой коммит (фикс):
    fix: resolve token validation issue in auth module
 
 Коммит с указанием области (scope):
    feat(parser): add support for async functions
 
 Развернутый коммит с описанием и ссылкой на задачу:
    feat: add social media login options

     - implement Google OAuth2 provider
     - add login buttons to the landing page
     - update user schema to store provider ID

     Fixes #123

 Критическое изменение (Breaking Change):
 Добавляется восклицательный знак после типа или пометка в футере.
     feat!: change API endpoint for user profile retrieval

     BREAKING CHANGE: the old /api/user endpoint is now deprecated.

 
 Полезные советы:
 
     Атомарность: один коммит должен содержать только одно логическое изменение. Если вы правите баг и добавляете фичу, разделите это на два коммита.
     Язык: в международной среде и Open Source проектах принято использовать английский язык.
     Ссылки: используйте ключевые слова Fixes #123 или Closes #123, чтобы GitHub автоматически закрывал связанные задачи при слиянии веток.

 */

